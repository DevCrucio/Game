package com.game.packet;

public class Color {
	private float r, g, b;

	public void set(float r, float g, float b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public float getB() {
		return b;
	}

	public float getG() {
		return g;
	}

	public float getR() {
		return r;
	}
}
